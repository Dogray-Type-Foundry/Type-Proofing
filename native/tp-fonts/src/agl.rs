/// Adobe Glyph List fallback: maps glyph names to Unicode characters.
/// Only used when cmap is empty (rare). Handles the uniXXXX convention
/// and a subset of common AGL names.
pub fn agl_to_unicode(name: &str) -> Option<char> {
    // uniXXXX convention
    if name.starts_with("uni") && name.len() == 7 {
        let hex = &name[3..];
        let cp = u32::from_str_radix(hex, 16).ok()?;
        return char::from_u32(cp);
    }

    // uXXXXX convention (5+ hex digits)
    if name.starts_with('u') && name.len() >= 5 && name[1..].chars().all(|c| c.is_ascii_hexdigit()) {
        let cp = u32::from_str_radix(&name[1..], 16).ok()?;
        return char::from_u32(cp);
    }

    // Common AGL names
    AGL_MAP.iter().find(|(n, _)| *n == name).map(|(_, ch)| *ch)
}

